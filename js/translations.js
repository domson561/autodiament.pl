// Funkcja do prze≈ÇƒÖczania jƒôzyka na rosyjski
function switchToRussian() {
    document.querySelectorAll('[data-ru]').forEach(element => {
        const key = element.getAttribute('data-ru');
        if (translations.ru[key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations.ru[key];
            } else {
                element.innerHTML = translations.ru[key];
            }
        }
    });
    
    // Zapisz preferencjƒô jƒôzyka
    localStorage.setItem('language', 'ru');
    
    // Zmie≈Ñ jƒôzyk w tagu html
    document.documentElement.lang = 'ru';
}

// Funkcja do przywr√≥cenia oryginalnego jƒôzyka (PL lub DE)
function restoreOriginalLanguage() {
    localStorage.removeItem('language'); // Usu≈Ñ preferencjƒô jƒôzyka
    location.reload(); // Prze≈Çaduj stronƒô
}

